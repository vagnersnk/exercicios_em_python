#João tem um problema com os números.
# Ele sempre escreve números de quatro algarismos de trás para frente
# (o número 1234 ele escreve 4321).
# Escreva um algoritmo que imprima invertidos os números de quatro algarismos.

valor = input("Digite um valor de 4 algarismos: ")
print("Sequência inversa:")
print(valor[::-1])
